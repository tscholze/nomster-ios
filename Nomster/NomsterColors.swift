import UIKit

class NomsterColors {
    class func green() -> UIColor {
        return UIColor (
            red: 0x33 / 255,
            green: 0x99 / 255,
            blue: 0x66 / 255,
            alpha: 1.0
        )
    }
    
    class func blue() -> UIColor {
        return UIColor (
            red: 0x3A / 255,
            green: 0x67 / 255,
            blue: 0xAB / 255,
            alpha: 1.0
        )
    }
}